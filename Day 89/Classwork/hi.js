//  შექმენით while loop, სადაც გამოიტანთ 1-იდან 10-მდე რიცხვებს
let i = 0
while (i <= 10){
    console.log(i);
    i ++;
}
//შექმენით while loop, სადაც გამოიტანთ 'hello'-ს 5-ჯერ.
let u = 0
while (u <= 5){
    console.log("hello");
    u ++;
}
// შექმენით for loop, სადაც გამოიტანთ 2-იდან 6-მდე რიცხვებს.
for (let hi = 2; hi <= 6; hi ++){
    console.log(hi);
}
//შექმენით for loop, სადაც გამოიტანთ 'hello'-ს 6-ჯერ.
for (let tired = 0; tired < 6; tired ++) {
    console.log('hello');
}
